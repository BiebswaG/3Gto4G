    nicknamed = namegen.namegen(nickname) != stats.species.get(species).upper()
    ability = abilities.get(species)

    species = chr(species & 0xff) + chr((species >> 8) & 0xff)

    item = gba[34:36]

    exp = gba[36:40]

    ppup = ord(gba[40])
    ppupa = chr((ppup >> 0) & 3) 
    ppupb = chr((ppup >> 2) & 3)
    ppupc = chr((ppup >> 4) & 3)
    ppupd = chr((ppup >> 6) & 3)

    happy = gba[41]

    atk1 = gba[44:46]
    atk2 = gba[46:48]
    atk3 = gba[48:50]
    atk4 = gba[50:52]
	
